#include "ft_printf.h"

void ft_puthex(unsigned long n , int *c)
{
    if(n < 16)
    {
        ft_puthex("0123456789abcdef"[n], c);
        
    }
    ft_putchar("0123456789abcdef"[n % 16], c);
}